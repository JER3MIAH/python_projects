# A list is used to store multiple items within a single variable

hobbies = ["coding", "anime", "workouts", "games", "dancing"]

hobbies[4] = "food"  # since I actually don't like dancing

hobbies.append("drinking water")  # adds an item to the end of my list

hobbies.sort()  # this sorts the lists alphabetically

print(hobbies)

for i in hobbies:
    print(i)
